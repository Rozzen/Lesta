def IsEven2(value):
    return (value & 1) == 0
    # Плюсы:
    # При работе с большими данными производительность страдать не будет, так как это простая битовая операция.
    # Код короче и может быть более производительным на уровне низкоуровневых операций.
    # Минусы:
    # Менее интуитовно понятен при чтении в контексте всего кода

def IsEven3(value):
    return np.mod(value, 2) == 0
    # Плюсы:
    # Можно поступить по умному и воспользоваться методом mod если в проекте используется библиотека numpy
    # Может быть быстрее для обработки больших массивов данных.
    # Минусы:
    # Требует установки и импорта numpy, что увеличивает размер проекта и сложность решения задачи.

